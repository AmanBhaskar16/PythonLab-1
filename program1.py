# Basic arithmetic operations in python

a = int(input("Enter 1st number : "));
b = int(input("Enter 2nd number : "));
print("The sum is : ");
print(a+b);
print("The product is : ");
print(a*b);
print("The fraction quotient is : ");
print(a/b);
print("The subtraction is : ");
print(a-b);
print("The integer quotient is : ");
print(a//b);
print("The remainder is : ");
print(a%b);
